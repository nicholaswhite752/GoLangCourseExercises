package main

import "fmt"

func main() {

	x := 42
	y := 66

	//Comparison operators
	g := x == y
	h := x <= y
	i := x >= y
	j := x != y
	k := x < y
	l := x > y

	fmt.Println("g: ", g)
	fmt.Println("h: ", h)
	fmt.Println("i: ", i)
	fmt.Println("j: ", j)
	fmt.Println("k: ", k)
	fmt.Println("l: ", l)

}
